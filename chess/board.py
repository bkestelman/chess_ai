class Board:
    def __init__(self):
        self.board = [
                [ '-',  '-',  '-',  '-',  '-',  '-',  '-',  '-'], 
                [ '-',  '-',  '-',  '-',  '-',  '-',  '-',  '-'], 
                [ '-',  '-',  '-',  '-',  '-',  '-',  '-',  '-'], 
                [ '-',  '-',  '-',  '-',  '-',  '-',  '-',  '-'], 
                [ '-',  '-',  '-',  '-',  '-',  '-',  '-',  '-'], 
                [ '-',  '-',  '-',  '-',  '-',  '-',  '-',  '-'], 
                [ '-',  '-',  '-',  '-',  '-',  '-',  '-',  '-'], 
                [ '-',  '-',  '-',  '-',  '-',  '-',  '-',  '-'], 
                ]

    def __repr__(self):
        board = '\n'.join(['\t'.join([str(square) for square in row]) for row in self.board])
        tab_length = 8 # python default 
        return board + '\n' + ('-' * tab_length * 7)

